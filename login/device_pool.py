def pick_device() -> dict:
    """
    从池子里随机抽一条设备。
    每次重新随机 androidId 保证唯一性。
    device_id 和 device_fp 由调用方（mobile_login.py）负责填充：
      - device_id : make_device_id()
      - device_fp : fingerprint.fetch_device_fp() 的返回值
    """
    d = dict(random.choice(_DEVICE_POOL))
    d["androidId"] = uuid.uuid4().hex[:16]
    d["device_id"] = ""   # 占位，由 mobile_login.py 填充
    d["device_fp"] = ""   # 占位，由 mobile_login.py 填充
    return d